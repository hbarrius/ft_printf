#include "libft.h"

int		ft_atoi(const char *str)
{
	int					i;
	long				neg;
	unsigned long long	num;

	i = 0;
	neg = 1;
	num = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (i >= 19)
			return (neg == -1 ? 0 : -1);
		num = num * 10 + str[i] - '0';
		i++;
	}
	return (num * neg);
}
